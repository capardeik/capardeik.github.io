function ShowFamily() {
    document.getElementById("familyexpand").style.display="block";
    document.getElementById("familyexpandbutton").style.display="none";
    document.getElementById("familycollapsebutton").style.display="block";
}
function HideFamily() {
    document.getElementById("familyexpand").style.display="none";
    document.getElementById("familyexpandbutton").style.display="block";
    document.getElementById("familycollapsebutton").style.display="none";
}